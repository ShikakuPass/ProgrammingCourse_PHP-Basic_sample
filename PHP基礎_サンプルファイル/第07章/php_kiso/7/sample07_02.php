<?php
$x = 10;
$y = 20;

$x += $y; //$x = $x + $y
echo "加算結果：" . $x . "<br>";

$x -= $y; //$x = $x - $y
echo "減算結果：" . $x . "<br>";

$x *= $y; //$x = $x * $y
echo "乗算結果：" . $x . "<br>";

$x /= $y; //$x = $x / $y
echo "除算結果：" . $x . "<br>";

$x %= $y; //$x = $x % $y
echo "剰余結果：" . $x . "<br>";
?>